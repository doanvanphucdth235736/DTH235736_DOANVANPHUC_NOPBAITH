# Ý nghĩa các toán tử trong Python

a = 10
b = 3

print("a / b =", a / b)      # Toán tử chia lấy phần thực (float division)
print("a // b =", a // b)    # Toán tử chia lấy phần nguyên (integer division)
print("a % b =", a % b)      # Toán tử lấy phần dư (modulo)
print("a ** b =", a ** b)    # Toán tử lũy thừa (power)

x = True
y = False

print("x and y =", x and y)  # Toán tử logic AND
print("x or y =", x or y)    # Toán tử logic OR

c = [1, 2, 3]
d = c
e = [1, 2, 3]

print("c is d =", c is d)    # Kiểm tra hai biến có cùng tham chiếu không
print("c is e =", c is e)    # Kiểm tra hai biến có cùng tham