# Ví dụ về các loại ghi chú trong Python

# Đây là ghi chú một dòng

"""
Đây là ghi chú nhiều dòng.
Có thể dùng để mô tả hàm, lớp, hoặc đoạn mã.
"""

def add(a, b):
    """
    Hàm cộng hai số a và b.
    Trả về tổng của a và b.
    """
    return a + b

print(add(2, 3))  # In ra kết